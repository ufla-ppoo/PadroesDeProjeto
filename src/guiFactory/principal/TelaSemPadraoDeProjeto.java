    private void construirTela() {
        JPanel painel = new PainelDoCruzeiro();
        JLabel rotulo = new RotuloDoCruzeiro();
        rotulo.setText("Digite aqui seu nome: ");
        JTextField caixaDeTexto = new CaixaTextoDoCruzeiro();
        caixaDeTexto.setColumns(5);
        JButton botao = new BotaoDoCruzeiro();
        botao.setText("Enviar");        
        
        this.setTitle("Tela Inicial");
        this.setContentPane(painel);
        this.add(rotulo);
        this.add(caixaDeTexto);
        this.add(botao);
    }

